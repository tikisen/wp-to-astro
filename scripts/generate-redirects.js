#!/usr/bin/env node
// scripts/generate-redirects.js
// Generates a Cloudflare Pages _redirects file from the crawl sitemap.
// Edit the RENAMES map below before running.
// Writes: extraction/redirects.txt

import { readFileSync, writeFileSync } from 'fs';

const sitemap = JSON.parse(readFileSync('extraction/sitemap.json', 'utf8'));

// Add any URL path changes here: oldPath -> newPath
const RENAMES = {
  // '/old-path/': '/new-path/',
};

const lines = ['# Generated by wp-to-astro generate-redirects.js', '# Edit RENAMES in the script to add custom redirects', ''];

for (const [from, to] of Object.entries(RENAMES)) {
  lines.push(`${from} ${to} 301`);
}

// WordPress common URL patterns → Astro equivalents
lines.push('/wp-login.php /  302');
lines.push('/wp-admin/* /  302');
lines.push('/feed/ /  301');
lines.push('/feed/rss/ /  301');
lines.push('/wp-content/uploads/* /assets/:splat 301');

writeFileSync('extraction/redirects.txt', lines.join('\n'));
console.log('Wrote extraction/redirects.txt');
console.log('Copy to public/_redirects in your Astro project before deploying.');
